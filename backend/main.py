@app.delete("/api/s3/buckets/{bucket_uuid}", status_code=204)
async def delete_s3_bucket_route(
    bucket_uuid: str,
    environment: str = Query(...),
    db: Session = Depends(get_db),
    current_user=Depends(get_current_user_required()),
):
    """
    Delete an S3 bucket off NetApp targeting its active UUID string.
    """
    if bucket_uuid == "pending":
        raise HTTPException(
            status_code=400, 
            detail="Cannot delete a bucket that is actively resolving its UUID. Please refresh the grid."
        )

    # 1. Enforce local environment boundaries
    db_buckets = get_s3_buckets(db, current_user, environment)
    target_bucket = next((b for b in db_buckets if b.bucket_uuid == bucket_uuid), None)
    if not target_bucket:
        raise HTTPException(status_code=404, detail="Bucket tracking not found or you lack ownership permissions.")

    if target_bucket.deletion == "pending":
        raise HTTPException(status_code=400, detail="Bucket is already queued for background deletion natively.")

    cfg = _get_svm_or_404(environment)
    netapp_url = f"{cfg.base_url}/protocols/s3/buckets/{cfg.svm_uuid}/{bucket_uuid}"

    # 2. Fire the network cancellation command to SVM
    try:
        async with get_netapp_client(cfg, timeout=30) as client:
            # 2a: Scrape and physically destroy orphaned proxy Groups dynamically to allow ONTAP to drop the policies cleanly
            try:
                group_url = f"{cfg.base_url}/protocols/s3/services/{cfg.svm_uuid}/groups"
                res = await client.get(group_url, headers=_netapp_headers())
                if res.status_code == 200:
                    grps = res.json().get("records", [])
                    for g in grps:
                        gname = g.get("name", "")
                        if gname in (f"GrpFullAccess_{target_bucket.name}", f"GrpReadOnly_{target_bucket.name}"):
                            gid = g.get("id")
                            if gid:
                                await client.delete(f"{group_url}/{gid}", headers=_netapp_headers())
            except Exception as e:
                print(f"Silently failed dropping dynamic groups during bucket wipe: {e}")

            # 2b: Drop the explicitly mapped static Policies
            try:
                policy_url = f"{cfg.base_url}/protocols/s3/services/{cfg.svm_uuid}/policies"
                await client.delete(f"{policy_url}/FullAccess_{target_bucket.name}", headers=_netapp_headers())
                await client.delete(f"{policy_url}/ReadOnly_{target_bucket.name}", headers=_netapp_headers())
            except Exception as e:
                print(f"Silently failed dropping policies during bucket wipe: {e}")
                
            # 2c: Execute master Bucket payload override
            response = await client.delete(netapp_url, headers=_netapp_headers())
    except httpx.RequestError as exc:
        raise HTTPException(status_code=502, detail=f"Could not reach NetApp SVM API: {exc}")

    # NetApp physically blocked deletion, likely because the bucket still actively houses file objects!
    if response.status_code == 400:
        raise HTTPException(
            status_code=400,
            detail="NetApp blocked bucket deletion! Ensure the bucket is entirely empty of objects before deleting."
        )
        
    if response.status_code not in (200, 204, 404, 202):
        raise HTTPException(status_code=502, detail=f"NetApp SVM API returned {response.status_code}: {response.text}")

    # 3. Synchronize local storage to a pending deletion sweep state
    mark_s3_bucket_deleting(db, current_user, environment, bucket_uuid)
    return None


