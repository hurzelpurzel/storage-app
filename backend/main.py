@app.get(
    "/api/s3/users",
    response_model=S3UserListResponse,
    responses={404: {"model": ErrorResponse}, 500: {"model": ErrorResponse}},
)
async def list_s3_users(
    environment: str = Query(..., description="SVM environment name, e.g. DEV/TEST"),
    db: Session = Depends(get_db),
    current_user=Depends(get_current_user_required()),
):
    """
    List all S3 users for the given environment stored in the local database.
    Also refreshes each user's details from NetApp so the UI always shows
    up-to-date access_key and key_expiry_time.
    """
    # Validate environment is configured
    cfg = _get_svm_or_404(environment)

    try:
        db_users = get_s3_users(db, owner_email=current_user, environment=environment)
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))

    # Refresh details from NetApp for each user (best-effort — don't fail the
    # whole list if a single NetApp call fails).
    refreshed = []
    async with get_netapp_client(cfg, timeout=15) as client:
        for user in db_users:
            netapp_url = (
                f"{cfg.base_url}/protocols/s3/services"
                f"/{cfg.svm_uuid}/users/{user.username}"
            )
            try:
                resp = await client.get(netapp_url, headers=_netapp_headers())
                if resp.status_code == 200:
                    data = resp.json()
                    # NetApp may wrap the result in a records array
                    if "records" in data and data["records"]:
                        record = data["records"][0]
                    else:
                        record = data
                    # Update local fields if NetApp returned them
                    if record.get("access_key"):
                        user.access_key = record["access_key"]
                    if record.get("key_expiry_time") is not None:
                        user.key_expiry_time = record["key_expiry_time"]
            except Exception:
                pass  # Use locally stored data on failure
            refreshed.append(user)

    return {"data": refreshed}


@app.post(
    "/api/s3/users",
    response_model=S3UserCreateResponse,
    status_code=status.HTTP_201_CREATED,
    responses={
        400: {"model": ErrorResponse},
        404: {"model": ErrorResponse},
        409: {"model": ErrorResponse},
        502: {"model": ErrorResponse},
        500: {"model": ErrorResponse},
    },
)
async def create_new_s3_user(
    payload: S3UserCreate,
    db: Session = Depends(get_db),
    current_user=Depends(get_current_user_required()),
):
    """
    Create a new S3 user on the NetApp SVM for the specified environment.

    NetApp POST /api/protocols/s3/services/{svm.uuid}/users
    Expected response (s3_user_post_patch_response):
      { "num_records": 1, "records": [{ "access_key": "...", "secret_key": "...",
                                        "name": "...", "key_expiry_time": "..." }] }

    The access_key (and optional key_expiry_time) are stored in the database.
    The secret_key is returned to the caller exactly once and never persisted.
    """
    cfg = _get_svm_or_404(payload.environment)

    # Check for duplicate username within this environment
    if get_s3_user_by_username(db, payload.environment, payload.username):
        raise HTTPException(
            status_code=409,
            detail=(
                f"S3 user '{payload.username}' already exists "
                f"in environment '{payload.environment}'."
            ),
        )

    netapp_url = (
        f"{cfg.base_url}/protocols/s3/services/{cfg.svm_uuid}/users"
    )
    netapp_body: dict = {"name": payload.username}
    if payload.comment:
        netapp_body["comment"] = payload.comment

    try:
        async with get_netapp_client(cfg, timeout=30) as client:
            response = await client.post(
                netapp_url,
                json=netapp_body,
                headers=_netapp_headers(),
            )
    except httpx.RequestError as exc:
        raise HTTPException(
            status_code=502,
            detail=f"Could not reach NetApp SVM API: {exc}",
        )

    if response.status_code not in (200, 201):
        raise HTTPException(
            status_code=502,
            detail=f"NetApp SVM API returned {response.status_code}: {response.text}",
        )

    try:
        netapp_data = response.json()
        # NetApp wraps results in a records array
        records = netapp_data.get("records", [])
        if not records:
            raise ValueError("Empty records array in NetApp response")
        record = records[0]
        access_key: str = record["access_key"]
        secret_key: str = record["secret_key"]
        key_expiry_time: Optional[str] = record.get("key_expiry_time")
    except (ValueError, KeyError) as exc:
        raise HTTPException(
            status_code=502,
            detail=f"Unexpected response from NetApp SVM API: {exc}",
        )

    # Persist user (without the secret)
    db_user = create_s3_user(
        db,
        owner_email=current_user,
        environment=payload.environment,
        username=payload.username,
        access_key=access_key,
        comment=payload.comment,
        key_expiry_time=key_expiry_time,
    )

    return S3UserCreateResponse(
        id=db_user.id,
        owner_email=db_user.owner_email,
        environment=db_user.environment,
        username=db_user.username,
        comment=db_user.comment,
        access_key=db_user.access_key,
        key_expiry_time=db_user.key_expiry_time,
        created_at=db_user.created_at,
        secret_key=secret_key,
    )

@app.delete("/api/s3/users/{username}", status_code=204)
async def delete_s3_user_route(
    username: str,
    environment: str = Query(...),
    db: Session = Depends(get_db),
    current_user=Depends(get_current_user_required()),
):
    """
    Delete an S3 user from the NetApp SVM and sync the local SQLite store.
    """
    # 1. Enforce ownership tracking natively in the DB query logic
    local_user = get_s3_user_by_username(db, environment, username)
    if not local_user or local_user.owner_email != current_user:
        raise HTTPException(status_code=404, detail="S3 user not found or you lack ownership permissions.")

    cfg = _get_svm_or_404(environment)
    netapp_url = f"{cfg.base_url}/protocols/s3/services/{cfg.svm_uuid}/users/{username}"

    # 2. Fire the network cancellation command to SVM
    try:
        async with get_netapp_client(cfg, timeout=30) as client:
            response = await client.delete(netapp_url, headers=_netapp_headers())
    except httpx.RequestError as exc:
        raise HTTPException(
            status_code=502,
            detail=f"Could not reach NetApp SVM API: {exc}",
        )

    # Allow 404 from NetApp (already deleted on server)
    if response.status_code not in (200, 204, 404):
        raise HTTPException(
            status_code=502,
            detail=f"NetApp SVM API returned {response.status_code}: {response.text}",
        )

    # 3. Synchronize local storage wipe
    delete_s3_user(db, current_user, environment, username)
    return None

@app.post("/api/s3/buckets", status_code=201, response_model=S3BucketSchema)
async def create_new_s3_bucket(
    payload: S3BucketCreate,
    db: Session = Depends(get_db),
    current_user=Depends(get_current_user_required()),
):
    """
    Create a new S3 bucket on the NetApp SVM and track it locally.
    """
    # Guard against duplicate active names inside this environment natively
    active_buckets = get_s3_buckets(db, current_user, payload.environment)
    if any(b.name == payload.name for b in active_buckets):
        raise HTTPException(status_code=409, detail="Bucket tracking name already localized to this environment.")

    cfg = _get_svm_or_404(payload.environment)
    netapp_url = f"{cfg.base_url}/protocols/s3/buckets"
    
    netapp_body = {
        "name": payload.name,
        "svm": {
            "name": cfg.svm_name
        }
    }

    try:
        async with get_netapp_client(cfg, timeout=30) as client:
            response = await client.post(
                netapp_url,
                json=netapp_body,
                headers=_netapp_headers(),
            )
    except httpx.RequestError as exc:
        raise HTTPException(status_code=502, detail=f"Could not reach NetApp SVM API: {exc}")

    if response.status_code not in (200, 201, 202):
        raise HTTPException(status_code=502, detail=f"NetApp SVM API returned {response.status_code}: {response.text}")

    # Fire background Generation for IAM Policies natively bridging this Bucket
    try:
        async with get_netapp_client(cfg, timeout=30) as client:
            full_access_payload = {
                "name": f"FullAccess_{payload.name}",
                "statements": [{
                    "effect": "allow",
                    "actions": ["*"],
                    "resources": [payload.name, f"{payload.name}/*"]
                }]
            }
            read_only_payload = {
                "name": f"ReadOnly_{payload.name}",
                "statements": [{
                    "effect": "allow",
                    "actions": ["GetObject", "ListBucket"],
                    "resources": [payload.name, f"{payload.name}/*"]
                }]
            }
            policy_url = f"{cfg.base_url}/protocols/s3/services/{cfg.svm_uuid}/policies"
            # We don't block the frontend heavily if these fail, but we'll try natively hooking them
            await client.post(policy_url, json=full_access_payload, headers=_netapp_headers())
            await client.post(policy_url, json=read_only_payload, headers=_netapp_headers())
    except Exception as e:
        print(f"Failed generating Policies automatically behind the scenes for bucket {payload.name}: {e}")

    # Immediately lock async representation into database proxy
    db_bucket = create_s3_bucket(db, current_user, payload.environment, payload.name)
    return db_bucket


@app.get("/api/s3/buckets", response_model=S3BucketListResponse)
async def list_s3_buckets(
    environment: str,
    db: Session = Depends(get_db),
    current_user=Depends(get_current_user_required()),
):
    """
    Fetch S3 buckets for the environment, explicitly resolving pending async creations and trailing deletions natively.
    """
    cfg = _get_svm_or_404(environment)
    db_buckets = get_s3_buckets(db, current_user, environment)

    pending_creations = [b for b in db_buckets if b.bucket_uuid == "pending"]
    pending_deletions = [b for b in db_buckets if b.deletion == "pending"]
    
    if pending_creations or pending_deletions:
        try:
            async with get_netapp_client(cfg, timeout=30) as client:
                response = await client.get(f"{cfg.base_url}/protocols/s3/buckets", headers=_netapp_headers())
                if response.status_code == 200:
                    data = response.json()
                    netapp_records = data.get("records", [])
                    
                    active_environment_buckets = [
                        r for r in netapp_records 
                        if r.get("svm", {}).get("name", "") == cfg.svm_name
                    ]
                    active_uuids = {r.get("uuid") for r in active_environment_buckets}
                    
                    # Resolve Pending Creations
                    for p in pending_creations:
                        match = next((r for r in active_environment_buckets if r.get("name") == p.name), None)
                        if match:
                            update_s3_bucket_uuid(db, p.id, match.get("uuid"))
                            p.bucket_uuid = match.get("uuid")
                            
                    # Resolve Pending Deletions
                    for p in pending_deletions:
                        if p.bucket_uuid not in active_uuids:
                            timestamp = datetime.utcnow().isoformat()
                            mark_s3_bucket_deleted(db, p.id, timestamp)
                            p.deletion = timestamp

        except Exception as e:
            print(f"Bucket Async Resolution Failed: {str(e)}")

    # Filter out heavily timestamped buckets representing completed background deletes natively from the array
    filtered_buckets = [b for b in db_buckets if b.deletion in (None, "pending")]
    return S3BucketListResponse(data=filtered_buckets)


