@app.get("/api/auth/status")
async def auth_status():
    """
    Returns current auth configuration.
    When ENABLE_ENTRA_AUTH=false, also issues a ready-made JWT for the dev user
    so the frontend can auto-login without any redirect flow.
    """
    if not settings.enable_entra_auth:
        token = create_access_token({"sub": settings.dev_user})
        return {
            "auth_enabled": False,
            "dev_user": settings.dev_user,
            "access_token": token,
        }
    return {"auth_enabled": True}

@app.get("/api/auth/login-url")
async def get_login_url(code_challenge: str = Query(..., description="PKCE code_challenge (S256) generated by the browser")):
    """
    Return the Entra ID authorisation URL.
    The browser must supply a PKCE code_challenge; the matching code_verifier
    is stored in sessionStorage and sent back at /api/auth/callback.
    """
    if not settings.enable_entra_auth:
        return {"enabled": False}

    auth_url = get_entra_auth_url(code_challenge)
    return {"enabled": True, "login_url": auth_url}

class AuthCallbackRequest(BaseModel):
    code: str
    code_verifier: str

@app.post("/api/auth/callback")
async def auth_callback(body: AuthCallbackRequest):
    """Handle Entra ID authentication callback"""
    try:
        result = verify_entra_token(body.code, body.code_verifier)
        # Create a JWT token for the authenticated user
        username = (result.get("id_token_claims") or {}).get("preferred_username") \
            or (result.get("account") or {}).get("username", "user")
        access_token = create_access_token({"sub": username})
        return {"access_token": access_token, "token_type": "bearer", "username": username}
    except Exception as e:
        raise HTTPException(status_code=400, detail=str(e))

