def _get_svm_or_404(environment: str) -> SvmConfig:
    """Resolve environment name to SvmConfig or raise 404."""
    configs = settings.get_svm_configs()
    cfg = configs.get(environment)
    if not cfg:
        raise HTTPException(
            status_code=404,
            detail=f"Environment '{environment}' is not configured.",
        )
    return cfg


def _netapp_headers() -> dict:
    return {"Content-Type": "application/json", "Accept": "application/json"}


async def log_request(request: httpx.Request):
    if settings.trace_mode:
        command = f"curl -X {request.method} -H " + " -H ".join(f"'{k}: {v}'" for k, v in request.headers.items()) + f" '{request.url}'"
        if request.content:
            try:
                command += f" -d '{request.content.decode()}'"
            except Exception:
                command += " -d '<binary>'"
        print(f"\\n[TRACE] Request: {command}")

async def log_response(response: httpx.Response):
    if settings.trace_mode:
        await response.aread()
        print(f"[TRACE] Response {response.status_code}: {response.text}\\n")

def get_netapp_client(cfg: SvmConfig, timeout: int = 30) -> httpx.AsyncClient:
    return httpx.AsyncClient(
        auth=(cfg.username, cfg.password),
        verify=settings.netapp_verify_ssl,
        timeout=timeout,
        event_hooks={'request': [log_request], 'response': [log_response]}
    )


